# Iследовалось влияние препарата на уровень давления пациентов. Сначала измерялось давление до приема препарата, потом через 10 минут и через 30 минут. Есть ли статистически значимые различия?

# 1е измерение до приема препарата: 150, 160, 165, 145, 155
# 2е измерение через 10 минут: 140, 155, 150,  130, 135
# 3е измерение через 30 минут: 130, 130, 120, 130, 125


import scipy.stats as stats

# Задаем выборки
before = [150, 160, 165, 145, 155]
after_10min = [140, 155, 150, 130, 135]
after_30min = [130, 130, 120, 130, 125]

# Выполняем критерий Фридмана
statistic, p_value = stats.friedmanchisquare(before, after_10min, after_30min)

# Выводим результаты
print("Статистика критерия Фридмана:", statistic)
print("p-значение:", p_value)

# Проверяем статистическую значимость различий
alpha = 0.05  # Уровень значимости

if p_value < alpha:
    print("Есть статистически значимые различия между выборками.")
else:
    print("Нет статистически значимых различий между выборками.")
